#A família Silva vai passar as férias na Europa e quer saber quantos euros vale o dinheiro em real que ela levará. 
#Dados um valor na moeda real e a cotação do euro, qual o valor correspondente em euros?
real = int(input("digite quantos reais a família Silva vai levar "))
euro = 0.19
din = real * euro
print("A família Silva tem ", real, "em real e ", din, "em euros")